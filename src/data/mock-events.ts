const now = new Date();
const today = now.toISOString().split("T")[0];

function hoursFromNow(h: number): string {
  const d = new Date(now.getTime() + h * 60 * 60 * 1000);
  return d.toISOString();
}

function hoursAgo(h: number): string {
  const d = new Date(now.getTime() - h * 60 * 60 * 1000);
  return d.toISOString();
}

